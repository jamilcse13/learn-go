package main

import "fmt"

func main()  {
	fmt.Println("Bismillah")

	fmt.Println("Go" + "Lang")
	fmt.Println("10+7 = ", 10+7)
	fmt.Println("7.0/3.0 = ", 7.0/3.0)

	// comment
	fmt.Println(true)	// this is also a comment
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	/*
	this is multi line comment
	*/
}

